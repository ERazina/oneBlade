<script>

    $('[data-ytid]').click(function (e) {
        var videoPlaceholder = $(this).find('.yt-placeholder');

        $(this).removeClass('shown');
        $(this).find('.video-item__play').fadeOut();
        ytPlayback($(this).data('ytid'), videoPlaceholder);
    });

    $('.flickity-prev-next-button').on('click',function(){
        $('[data-ytid]').addClass('shown');
        $('.video-item__play').fadeIn();
        window.main_ytplayer && window.main_ytplayer.destroy();
        delete window.main_ytplayer;
    });



    function ytPlayback(ytid, placeholder_selector) {
        /*window.main_ytplayer && window.main_ytplayer.destroy();
        delete window.main_ytplayer;*/
        window.main_ytplayer = new YT.Player($(placeholder_selector).get(0), {
            width: '100%', height: '100%',
            playerVars: {autoplay: 1, showinfo: 0, rel: 0, modestbranding: 1}
            , events: {
                onReady: function (e) {
                    var plist = ytid.split(',');
                    window.main_ytplayer.mute();
                    if (plist.length > 1) {
                        window.main_ytplayer.loadPlaylist(plist);
                    } else {
                        window.main_ytplayer.loadVideoById(ytid);
                    }
                },
                onStateChange: function (e) {
                    if (e.data == YT.PlayerState.ENDED) {
                        window.main_ytplayer && window.main_ytplayer.destroy();
                        delete window.main_ytplayer;
                        $('.video-item__play').fadeIn();
                        $('[data-ytid]').addClass('shown');

                    }
                }
            }
        });
    }
</script>

